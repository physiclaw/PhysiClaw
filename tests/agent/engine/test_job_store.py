# ---------- helpers ----------


def _job_section(
    *,
    job_id: str = "user-greet-2026-04-28",
    description: str = "Say good morning to the user",
    kind: str = "periodic",
    status: str = "pend",
    schedule: str = "0 7 * * *",
    context: str = "ten char minimum context",
    create_time: str = "2026-04-28T07:00",
    next_fire_time: str = "2026-04-29T07:00",
    last_fire_time: str = NEVER,
    execution_time: str = NEVER,
    execution_result: str = NEVER,
) -> str:
    return dedent(
        f"""\
        ## {job_id}
        {description}
        - Type: {kind}
        - Status: {status}
        - Schedule: `{schedule}`
        - Context: {context}
        - Create time: {create_time}
        - Next fire time: {next_fire_time}
        - Last fire time: {last_fire_time}
        - Execution time: {execution_time}
        - Execution result: {execution_result}
        """
    )


def _write_jobs(tmp_path: Path, *sections: str, header: str = "") -> Path:
    p = tmp_path / "jobs.md"
    p.write_text((header + "\n" if header else "") + "\n".join(sections))
    return p


