def test_find_due_continues_past_skipped_jobs_to_collect_later_due_ones() -> None:
    # Each `continue` in find_due must NOT be a `break` — a non-pend
    # job, an empty-next_fire_time pend job, and a malformed-iso pend
    # job all sit AHEAD of a real due job in the list. Break would
    # short-circuit and miss the due job.
    now = dt.datetime(2026, 4, 28, 12, 0)
    skipped_status = Job(
        id="a", kind=KIND_PERIODIC, schedule="* * * * *", description="d",
        status=STATUS_FIRED, next_fire_time="2026-04-28T11:00",
    )
    skipped_no_time = Job(
        id="b", kind=KIND_PERIODIC, schedule="* * * * *", description="d",
        next_fire_time="",
    )
    skipped_bad_iso = Job(
        id="c", kind=KIND_PERIODIC, schedule="* * * * *", description="d",
        next_fire_time="not iso",
    )
    real_due = Job(
        id="due", kind=KIND_PERIODIC, schedule="* * * * *", description="d",
        next_fire_time="2026-04-28T11:00",
    )

    out = find_due([skipped_status, skipped_no_time, skipped_bad_iso, real_due], now)

    assert out == [real_due]


