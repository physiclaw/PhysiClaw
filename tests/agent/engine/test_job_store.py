def test_update_fields_handles_multiple_jobs(tmp_path: Path) -> None:
    p = _write_jobs(
        tmp_path,
        _job_section(job_id="job-a"),
        _job_section(job_id="job-b"),
    )

    update_fields(
        p,
        {
            "job-a": {"Status": "fired"},
            "job-b": {"Status": "cancel"},
        },
    )

