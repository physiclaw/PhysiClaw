def test_purge_stale_uses_preloaded_jobs_list_when_supplied(
    tmp_path: Path,
) -> None:
    # The cron hook passes a pre-loaded jobs list to avoid re-parsing.
    # File on disk says one thing; the supplied list says another. The
    # supplied list wins for the staleness check.
    p = _write_jobs(
        tmp_path,
        _job_section(
            job_id="from-disk",
            status="done",
            next_fire_time=NEVER,
            execution_time="2026-04-26T00:00",  # young — wouldn't purge
            last_fire_time="2026-04-26T00:00",
        ),
    )
    preloaded = [
        Job(
            id="from-disk",
            kind=KIND_PERIODIC,
            schedule="0 7 * * *",
            description="d",
            status=STATUS_DONE,
            execution_time="2026-04-01T00:00",  # old per the supplied list
        )
    ]

    with freeze_time("2026-04-28T00:00"):
        purged = purge_stale(p, jobs=preloaded)

    assert purged == ["from-disk"]
